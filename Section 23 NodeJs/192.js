// Frameworks generally provides pre-built components, and structures that helps in building web applications, without needing to write every single line of code from scratch

// Whereas 'node' is not a framework, it's an asynchronous event-driven javascript runtime, it is used to build scalable network applications
// runtime => javascript code that we have written in browser is locked in jail, so when node came it liberated it from the jail, gave the freedom to allow us to use it to write any sort of application on desktop or server side
// So node provides with a javascript runtime that can run on a machine such as your local computer
// asynchronous => the code doesn't have to do anything sequentially, synchronous means to tie the resources until some sort of eventis resolved and then it will move forward
// event driven => in the form of events



