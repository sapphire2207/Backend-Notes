document.querySelector("button").classList;
//it gives all the classes that are attatched to the given element
document.querySelector("button").classList.add("invisible");// we are adding a class named invisible
document.querySelector("button").classList.remove("invisible");// we are removing a class named invisible
document.querySelector("button").classList.toggle("invisible");// we are removing a class named invisible if it is already present or adding the class when it is not present

