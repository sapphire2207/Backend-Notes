// Text Manipulation and the Text Content Property

document.querySelector("h1").innerHTML="Bye";//Bye -> this gives all information of the tag i.e gives the entire html inside the selector
document.querySelector("h1").textContent="Hello";//Hello -> it just gives you the content of the tag
document.querySelector("h1").innerHTML="Hello";//Hello

document.querySelector("h1").innerHTML="<em>YOO</em>";

