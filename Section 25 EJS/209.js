// EJS Tags:

// <%= variable %>  ==> whatever goes in bw the tag will be interpreted as javascript and will be displayed as output  
// <% console.log("Hello") %>  ==> this is executable javascript, but no output
// <%- <h1>Hello</h1> %>  ==> send html to ejs file, i.e render html
// <%%  %>  ==> Escape ejs tag, we can escape the ejs, i.e to show <% or %> on the website
// <%# This is a comment %>  ==> Used to write comments
// <%- include("FILE NAME - header.ejs")  %>  ==> insert another ejs file
